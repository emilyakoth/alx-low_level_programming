#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"

/**
 * op_add - op add
 * @a: first input
 * @b: second input
 *
 * Return: 0
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - returns a and b difference
 * @a: ...
 * @b: ...
 * Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies a & b
 * @a:...
 * @b:...
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide a & b
 * @a:...
 * @b:...
 * Return: 0
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns modulo
 * @a:...
 * @b:...
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}

